/*Higher Order Components 
-helps share common functionality between components without repeating code 
-HOC is a pattern where a function takes a component as an argument and returns a new component.
example syntax - const NewComponent = higherOrderComponent(originalComponent)
-HOC adds additional data or functionality to the original component, so the new component can also be referred to as
enhanced component.
example - const IronMan = withSuit(TonyStark);

 */